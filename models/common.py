class PatchEmbed(nn.Module):
    def __init__(self,
                 in_channels,
                 out_channels):
        super(PatchEmbed, self).__init__()
        self.bn = nn.BatchNorm2d(out_channels)
        self.pool2 = nn.AvgPool2d((2, 2), stride=2, ceil_mode=True, count_include_pad=False)
        self.conv = nn.Conv2d(in_channels, out_channels, kernel_size=1, stride=1, bias=False)
    def forward(self, x):

        y = self.bn(self.conv(self.pool2(x)))
        return y


class Mlp(nn.Module):
    def __init__(self, in_features,drop=0.):
        super().__init__()
        self.conv1 = nn.Conv2d(in_features, in_features//8, kernel_size=1, bias=True)
        self.act = nn.ReLU(inplace=True)
        self.conv2 = nn.Conv2d(in_features//8, in_features, kernel_size=1, bias=True)
        self.drop = nn.Dropout(drop)
        self.act1 = nn.Sigmoid()


    def forward(self, x):
        x = self.conv1(x)
        x = self.act(x)
        x = self.drop(x)
        x = self.conv2(x)
        x = self.drop(x)
        x = self.act1(x)
        return x



class DGFM(nn.Module):
    def __init__(self, in_channels, out_channels,  path_dropout=0,
                 drop=0, head_dim=32):
        super(DGFM, self).__init__()
        self.in_channels = in_channels
        self.out_channels = out_channels
        self.bn = nn.BatchNorm2d(out_channels)
        assert out_channels % head_dim == 0

        self.patch_embed = PatchEmbed(in_channels, out_channels)
        self.cffm = CFFM(out_channels,out_channels)
        self.attention_path_dropout = DropPath(path_dropout)

        self.mlp = Mlp(out_channels,  drop=drop)
        self.mlp_path_dropout = DropPath(path_dropout)
        self.cv = nn.Conv2d(2*out_channels, out_channels, 1)

    def forward(self, x):

        x = x + self.attention_path_dropout(self.cffm(x))
        x = x + self.mlp_path_dropout(self.mlp(x))
        return x




class CFFM(nn.Module):
    def __init__(self,c1, c2, b=1, gamma=2):
        super(CFFM, self).__init__()
        kernel_size = int(abs((math.log(c1, 2) + b) / gamma))
        kernel_size = kernel_size if kernel_size % 2 else kernel_size + 1

        self.pool_h = nn.AdaptiveAvgPool2d((None, 1))
        self.pool_w = nn.AdaptiveAvgPool2d((1, None))

        self.avg_pool = nn.AdaptiveAvgPool2d(1)
        self.conv = nn.Conv1d(1, 1, kernel_size=kernel_size, padding=(kernel_size - 1) // 2, bias=False)
        self.sigmoid = nn.Sigmoid()
        self.cv1 = nn.Conv2d(c1, c2, 1, 1)

    def forward(self, x):
        identity = x  # 1,64,128,128
        x_h = self.pool_h(x)  # 1,64,128,1
        x_w = self.pool_w(x)  # 1,64,1,128
        x_h = self.cv1(x_h)
        x_w = self.cv1(x_w)
        x_h = x_h.sigmoid()
        x_w = x_w.sigmoid()
        y = identity * x_h * x_w
        y = self.avg_pool(y)
        y = self.conv(y.squeeze(-1).transpose(-1, -2)).transpose(-1, -2).unsqueeze(-1)
        y = self.sigmoid(y)
        out = x * y.expand_as(x)
        return out




