class HLFM3(nn.Module):
    def __init__(self, c1, c2, n=1, shortcut=True, g=1, e=0.5):  # ch_in, ch_out, number, shortcut, groups, expansion
        super().__init__()
        self.c = int(c2 * e)  # hidden channels
        self.cv1 = Conv(c1, 2 * self.c, 1, 1)
        self.cv2 = Conv(4 * self.c, c2, 1)
        self.m = Bottleneck3(self.c, self.c, shortcut, g, k=((3, 3), (3, 3)), e=1)
        self.cv3 = DWConv(self.c, self.c,3,1)
        self.cv4 = DWConv(self.c, self.c, 1, 1)
    def forward(self, x):
        y = self.cv1(x)
        y = y.chunk(2, 1)
        identy1 = y[0]
        identy2 = y[1]
        x1 = self.cv4(self.cv3(identy1))
        x2 = self.m(identy2)
        y = self.cv2(torch.cat((y[0], y[1], x1, x2), 1))
        out = self.channel_shuffle(y,2)
        return out
    def channel_shuffle(self, x, groups):
        N, C, H, W = x.size()
        out = x.view(N, groups, C // groups, H, W).permute(0, 2, 1, 3, 4).contiguous().view(N, C, H, W)
        return out


